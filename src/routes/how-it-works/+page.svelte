	<div class="space-y-6 text-gray-700 text-sm leading-relaxed">
		<section>
			<h2 class="text-lg font-semibold text-gray-800 mb-2">What is Resistance Zero?</h2>
			<p>
				Resistance Zero (Re-zero) is a time management system created by Mark Forster.
				It is designed to gradually reduce the resistance to harder tasks as the "easy" tasks
				around them are completed.
			</p>
		</section>

		<section>
			<h2 class="text-lg font-semibold text-gray-800 mb-2">Core Principles</h2>
			<ul class="list-disc list-inside space-y-2">
				<li>
					<strong>Identify Zero Resistance:</strong> Read through your task list from bottom to top.
					Mark (dot) any task you feel absolutely no resistance to doing.
				</li>
				<li>
					<strong>Work from Bottom-Up:</strong> Action the dotted tasks starting from the end of the list.
				</li>
				<li>
					<strong>"Little and Often":</strong> You don't have to finish a task in one sitting.
					If you feel resistance building, stop and move to another task.
				</li>
				<li>
					<strong>Re-enter Tasks:</strong> If a task isn't completed, rewrite it at the end of the list.
				</li>
				<li>
					<strong>Rephrase Resistance:</strong> If a task consistently causes resistance,
					rephrase it to be smaller, such as "do 5 minutes on...".
				</li>
			</ul>
		</section>

		<section>
			<h2 class="text-lg font-semibold text-gray-800 mb-2">How to use this app</h2>
			<ol class="list-decimal list-inside space-y-3">
				<li>
					<strong>Add tasks:</strong> Type your tasks into the input field at the top.
					Each task is added to the end of the list.
				</li>
				<li>
					<strong>Scan for zero resistance:</strong> Read through your list from the bottom up.
					Click the circle next to any task you feel no resistance to doing. It will be "dotted" and move
					to the dotted section.
				</li>
				<li>
					<strong>Work through dotted tasks:</strong> In the dotted section, work through the tasks.
					Click "Done" when a task is finished.
				</li>
				<li>
					<strong>Re-enter unfinished tasks:</strong> If you didn't finish a task,
					click "Re-enter" to add it back to the end of the list with updated text.
					The original is marked as completed.
				</li>
				<li>
					<strong>Rephrase resistant tasks:</strong> If a task keeps causing resistance,
					click the edit icon to rephrase it into something smaller or more approachable.
				</li>
				<li>
					<strong>Repeat:</strong> After completing a pass, click "Undot all" to reset and
					scan again. The list naturally prioritizes itself as easy tasks get done and
					hard tasks get rephrased into manageable pieces.
				</li>
			</ol>
		</section>

		<section>
			<h2 class="text-lg font-semibold text-gray-800 mb-2">Why it works</h2>
			<p>
				Traditional to-do systems ask you to prioritize. Re-zero takes the opposite approach:
				always do what feels effortless right now. As easy tasks are removed, the tasks that
				once felt hard become the "easiest remaining" and resistance drops naturally. Rephrasing
				resistant tasks into smaller steps further lowers the barrier. Over time, everything gets done.
			</p>
		</section>
	</div>
</div>
